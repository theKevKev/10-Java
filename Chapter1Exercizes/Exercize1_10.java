package Chapter1Exercizes;

public class Exercize1_10 {
    public static void main(String[] args){
        DoubleCone();
        DoubleBox();
        System.out.println("|United| |United|");
        System.out.println("|States| |States|");
        DoubleBox();
        DoubleCone();
    }
    public static void DoubleCone() {
        System.out.println("   /\\       /\\   ");
        System.out.println("  /  \\     /  \\  ");
        System.out.println(" /    \\   /    \\ ");
    }
    public static void DoubleBox() {
        System.out.println("+------+ +------+");
        System.out.println("|      | |      |");
        System.out.println("|      | |      |");
        System.out.println("+------+ +------+");
    }
}
